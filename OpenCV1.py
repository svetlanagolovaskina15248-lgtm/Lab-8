import cv2


# ============================================================
# ФУНКЦИЯ: загрузка изображения
# ============================================================
def load_image(image_path: str):
    """
    Загружает изображение по указанному пути.

    Параметры:
        image_path (str): путь к изображению

    Возвращает:
        image: загруженное изображение
    """
    image = cv2.imread(image_path)

    # Проверяем, удалось ли загрузить изображение
    if image is None:
        raise FileNotFoundError(f"Не удалось найти или открыть файл: {image_path}")

    return image


# ============================================================
# ФУНКЦИЯ: перевод изображения в полутоновый формат
# ============================================================
def convert_to_grayscale(image):
    """
    Переводит цветное изображение в полутоновое.

    Параметры:
        image: исходное цветное изображение

    Возвращает:
        gray_image: полутоновое изображение
    """
    gray_image = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    return gray_image


# ============================================================
# ОСНОВНАЯ ФУНКЦИЯ ПРОГРАММЫ
# ============================================================
def main():
    """
    Основная логика программы:
    1. Загружаем изображение из папки images
    2. Переводим его в полутоновый формат
    3. Показываем исходное и преобразованное изображения
    4. Сохраняем результат в файл
    """
    image_path = "images/variant-1.jpg"
    # Если изображение в формате PNG, замените строку выше на:
    # image_path = "images/variant-1.png"

    # Загружаем исходное изображение
    original_image = load_image(image_path)

    # Переводим изображение в оттенки серого
    gray_image = convert_to_grayscale(original_image)

    # Показываем исходное изображение
    cv2.imshow("Original Image", original_image)

    # Показываем полутоновое изображение
    cv2.imshow("Grayscale Image", gray_image)

    # Сохраняем результат в отдельный файл
    cv2.imwrite("images/variant-1-gray.jpg", gray_image)

    print("Преобразование выполнено успешно.")
    print("Полутоновое изображение сохранено как images/variant-1-gray.jpg")

    # Ожидаем нажатия любой клавиши
    cv2.waitKey(0)

    # Закрываем все окна
    cv2.destroyAllWindows()


# ============================================================
# ТОЧКА ВХОДА В ПРОГРАММУ
# ============================================================
if __name__ == "__main__":
    main()